struct idt_entry  {
   uint16_t low_base; // offset bits 0..15
   uint16_t selector; // a code segment selector in GDT or LDT
   uint8_t zero;      // unused, set to 0
   uint8_t flags; // type and attributes, see below
   uint16_t high_base; // offset bits 16..31
}__attribute__((packed));
typedef struct idt_entry idt_entry;

struct idtr
{
	uint16_t size;
	uint32_t base;
	
}__attribute__((packed));
typedef struct idtr idtr;

idt_entry idt_entries[256];
idtr pIdt;

void createIdtEntry(uint16_t index, uint32_t base, uint16_t selector, uint8_t flags) 
{
	idt_entries[index].low_base = base & 0xFFFF;
	idt_entries[index].high_base = (base >> 16) & 0xFFFF;
	idt_entries[index].selector = selector;
	idt_entries[index].zero = 0;
	idt_entries[index].flags = flags;
}


